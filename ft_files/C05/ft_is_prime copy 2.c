#include <stdio.h>
int		ft_is_prime(int nb)
{
	if (((nb % 2) == 0) || ((nb % 3) == 0) || ((nb % 5) ==0))
		return (0);
	return (1);
}

int main() 
{
	 int nb = 2147483638;
	 printf("%d\n", ft_is_prime(nb));
}